<h1 style="font-size: 1.9em">uy-Grid</h1>
<h3 style="font-size: 1.4em">Servicio nacional de cómputo de alto
desempeño en plataforma Grid</h3>
<div id="content-body" style="text-align: justify;">
<p>El proyecto uy-Grid propone implementar un servicio de cómputo
científico de gran escala, basado en una infraestructura distribuida
(grid), instrumentando una plataforma de e-Ciencia que permita
satisfacer la demanda de cómputo científico a nivel nacional, tanto de
grupos de investigación como de empresas y otros actores del sector
productivo. La estrategia toma como modelo el actual servicio de cómputo
científico ofrecido por el Cluster-FING de la Facultad de Ingeniería,
para la creación de una plataforma computacional para cómputo científico
de alto desempeño, interconectada con el grid europeo-latinoamericano,
con la capacidad de proveer un poder de cómputo de gran escala.</p>
<div style='text-align: center;'><img
	src='<?php echo base_url();?>img/WhatIsGrid.jpg' style='margin: auto;' /></div>
<p>Tomando como base la experiencia previa en la instalación y gestión
de un servicio de cómputo local de mediano porte y la validación del
modelo de negocio utilizado para su gestión, se propone para el nuevo
servicio uy-Grid una escala sustancialmente mayor, que permita acceder a
un poder de cómputo para investigaciones científico- tecnológicas no
disponible en el país. La base fundamental de la propuesta consiste en
racionalizar el uso de los recursos aplicados a capacidad de cómputo
científico, a través del uso cooperativo y sustentable de una
infraestructura centralizada, alcanzando alta eficacia y eficiencia es
su utilización.</p>
<p>La instalación del servicio uy-Grid permitirá la integración a la
infraestructura grid Latinoamericana-Europea implementada por el
proyecto <a href='http://www.gisela-grid.eu/' target='_new'>GISELA</a>,
permitiendo el acceso de los investigadores nacionales a los recursos de
cómputo de esta plataforma distribuida, integrada por más de 50
instituciones en 15 países. El poder de cómputo que este servicio pondrá
a disposición de la comunidad científica y tecnológica uruguaya
permitirá mejorar decisivamente la capacidad de abordar en el ámbito
nacional la resolución de complejos problemas que modelan diversos
escenarios de aplicación directa en el entorno tecnológico e industrial
del país (como ejemplos pueden mencionarse la predicción y
racionalización del consumo energético, la investigación en
biotecnología y el diseño de fármacos, el estudio de modelos de
corrientes del Río de la Plata, entre otros).</p>
